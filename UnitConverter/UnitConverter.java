package UnitConverter;

public class UnitConverter {
	private String convertnumber;
	private double centimeter;
	private double inch;
	private double liter;
	private double gallon;
	private double celsius;
	private double fahrenheit;
	private double killogram;
	private double pound;
	
	public UnitConverter () {
		convertnumber = " ";
		centimeter = 0.0;
		inch = 0.0;
		liter = 0.0;
		gallon = 0.0;
		celsius = 0.0;
		fahrenheit = 0.0;
		killogram = 0.0;
		pound = 0.0;
	}

	public String getConvertnumber() {
		return convertnumber;
	}

	public void setConvertnumber(String convertnumber) {
		this.convertnumber = convertnumber;
	}

	public double getCentimeter() {
		return centimeter;
	}

	public void setCentimeter(double centimeter) {
		this.centimeter = centimeter;
	}

	public double getInch() {
		return inch;
	}

	public void setInch(double inch) {
		this.inch = inch;
	}

	public double getLiter() {
		return liter;
	}

	public void setLiter(double liter) {
		this.liter = liter;
	}

	public double getGallon() {
		return gallon;
	}

	public void setGallon(double gallon) {
		this.gallon = gallon;
	}

	public double getCelsius() {
		return celsius;
	}

	public void setCelsius(double celsius) {
		this.celsius = celsius;
	}

	public double getFahrenheit() {
		return fahrenheit;
	}

	public void setFahrenheit(double fahrenheit) {
		this.fahrenheit = fahrenheit;
	}

	public double getKillogram() {
		return killogram;
	}

	public void setKillogram(double killogram) {
		this.killogram = killogram;
	}

	public double getPound() {
		return pound;
	}

	public void setPound(double pound) {
		this.pound = pound;
	}
	
	

}
